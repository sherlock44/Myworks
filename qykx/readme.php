    <td><p>1. 发送短信</p>
      <p>本项目中应用的短信发送技术是通过Web service 来完成的,实现短信发送首先到新浪网上注册手机号，注册手机所用的地址是：http://sms.sina.com.cn,获取注册密码,然后就可以在本系统中使用注册的手机号码和密码进行短信发送.</p>
